const getAssetKey = (asset) =>
  `${String(asset?.chainFullName || asset?.queryChainName || "")
    .trim()
    .toLowerCase()}:${String(getAssetSymbol(asset) || "")
    .trim()
    .toLowerCase()}`;

export const normalizeAssetConfig = (asset) => {
  const {
    name,
    displayName,
    symbol,
    type,
    Decimals,
    decimals,
    chainFullName,
    contractAddress,
    queryChainName,
    queryChainShortName,
    shortName,
    coin_type,
    fee,
    ...rest
  } = asset || {};
  return {
    name: getAssetDisplayName(asset),
    symbol: getAssetSymbol(asset),
    type: getAssetType(asset),
    ...(Decimals != null ? { Decimals } : {}),
    ...(decimals != null ? { decimals } : {}),
    chainFullName: getAssetChainFullName(asset),
    ...(contractAddress !== undefined ? { contractAddress } : {}),
    ...rest,
    fee: fee ?? "",
    ...(assetConfigOverrides[getAssetKey(asset)] || {}),
  };
};

