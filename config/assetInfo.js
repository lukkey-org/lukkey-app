export const isHiddenInDevelopmentAsset = (asset) =>
  !RUNTIME_DEV &&
  hiddenInDevelopmentChainNames.has(
    String(getAssetChainFullName(asset) || "")
      .trim()
      .toLowerCase(),
  );

export const isRemovedAsset = (asset) =>
  removedAssetKeys.has(getAssetKey(asset));

export const initialAdditionalCryptos = configuredInitialAdditionalCryptos
  .filter(
    (asset) => !isHiddenInDevelopmentAsset(asset) && !isRemovedAsset(asset),
  )
  .map(normalizeAssetConfig);
